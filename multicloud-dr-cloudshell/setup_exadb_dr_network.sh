#!/usr/bin/env bash
set -Eeuo pipefail

# OCI Cloud Shell quick start:
# 1. Edit exadb_dr_network.conf in this same folder.
# 2. Run ./setup_exadb_dr_network.sh
# 3. Review exadb_dr_network.log and the generated exadb_dr_network_state_<timestamp>.env
#
# Manual verification checklist:
# - LPG peering in the primary region is PEERED.
# - LPG peering in the standby region is PEERED.
# - DRG RPC peering across regions is PEERED.
# - Existing Exadata VCN route tables point remote client/backup CIDRs to the local LPG.
# - Hub DRG attachment route tables point local client/backup CIDRs to the hub LPG.
# - Hub LPG route tables point remote client/backup CIDRs to the local DRG.
# - RPC DRG route tables point local client/backup CIDRs to the local hub VCN attachment.
# - NSGs exist in both Exadata VCNs.

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
CONFIG_FILE="${1:-$SCRIPT_DIR/exadb_dr_network.conf}"
LOG_FILE="$SCRIPT_DIR/exadb_dr_network.log"
RUN_TS="$(date -u +%Y%m%dT%H%M%SZ)"
STATE_FILE="$SCRIPT_DIR/exadb_dr_network_state_${RUN_TS}.env"
BUNDLE_VERSION="2026-03-16-parallel-regions-v1"
NAME_PREFIX_DEFAULT="exadb-dr-network"
DESCRIPTION_TOKEN=""
TMP_FILES=()
RPC_PEERING_PID=""
RPC_PEERING_STATUS_FILE=""

export OCI_CLI_SUPPRESS_FILE_PERMISSIONS_WARNING=True

touch "$LOG_FILE"

on_error() {
  local exit_code="$?"
  local line_no="${1:-unknown}"
  local cmd="${2:-unknown}"
  printf '%s [%s] Unexpected failure at line %s while running: %s\n' \
    "$(date -u +%Y-%m-%dT%H:%M:%SZ)" "ERROR" "$line_no" "$cmd" | tee -a "$LOG_FILE" >&2
  exit "$exit_code"
}

trap 'on_error "$LINENO" "$BASH_COMMAND"' ERR

log() {
  local level="$1"
  shift
  printf '%s [%s] %s\n' "$(date -u +%Y-%m-%dT%H:%M:%SZ)" "$level" "$*" | tee -a "$LOG_FILE"
}

fail() {
  log "ERROR" "$*"
  exit 1
}

cleanup_tmp() {
  if ((${#TMP_FILES[@]} > 0)); then
    rm -f "${TMP_FILES[@]}"
  fi
}

trap cleanup_tmp EXIT

require_cmd() {
  local cmd="$1"
  command -v "$cmd" >/dev/null 2>&1 || fail "Required command not found: $cmd"
}

make_tmp_json() {
  local tmp
  tmp="$(mktemp "$SCRIPT_DIR/exadb_dr_network_tmp_XXXXXX.json")"
  TMP_FILES+=("$tmp")
  printf '%s\n' "$tmp"
}

make_tmp_env() {
  local tmp
  tmp="$(mktemp "$SCRIPT_DIR/exadb_dr_network_tmp_XXXXXX.env")"
  TMP_FILES+=("$tmp")
  printf '%s\n' "$tmp"
}

persist_state_header() {
  cat >"$STATE_FILE" <<EOF
#!/usr/bin/env bash
# Generated by setup_exadb_dr_network.sh on ${RUN_TS}
# Source this file only for rollback automation.
EOF
}

persist_state() {
  local key="$1"
  local value="${2-}"
  printf '%s=%q\n' "$key" "$value" >>"$STATE_FILE"
}

normalize_bool() {
  local value="${1:-false}"
  case "${value,,}" in
    true|false) printf '%s\n' "${value,,}" ;;
    *) fail "ALLOW_SSH must be true or false, got: $value" ;;
  esac
}

normalize_port_or_empty() {
  local value="${1:-}"
  [[ -n "$value" ]] || { printf '\n'; return 0; }
  [[ "$value" =~ ^[0-9]+$ ]] || fail "Listener port must be a number, got: $value"
  ((value >= 1 && value <= 65535)) || fail "Listener port must be between 1 and 65535, got: $value"
  printf '%s\n' "$value"
}

sanitize_name_prefix() {
  local raw="${1:-$NAME_PREFIX_DEFAULT}"
  raw="$(printf '%s' "$raw" | tr '[:upper:]' '[:lower:]' | tr -cs 'a-z0-9-' '-')"
  raw="${raw##-}"
  raw="${raw%%-}"
  [[ -n "$raw" ]] || raw="$NAME_PREFIX_DEFAULT"
  printf '%s\n' "$raw"
}

oci_json() {
  local region="$1"
  shift
  oci --region "$region" "$@" --output json
}

oci_raw() {
  local region="$1"
  shift
  oci --region "$region" "$@" --raw-output
}

json_query_arg() {
  local json_input="$1"
  local py_code="$2"
  shift 2
  python3 -c "$py_code" "$json_input" "$@"
}

cidr_overlap_check() {
  local candidate="$1"
  shift
  python3 -c 'import ipaddress, json, sys
candidate = ipaddress.ip_network(sys.argv[1], strict=False)
for raw in sys.argv[2:]:
    if not raw:
        continue
    for entry in json.loads(raw):
        if not entry:
            continue
        if candidate.overlaps(ipaddress.ip_network(entry, strict=False)):
            raise SystemExit(1)
raise SystemExit(0)
' "$candidate" "$@"
}

resolve_hub_vcn_cidrs() {
  local primary_vcn_cidrs_json="$1"
  local standby_vcn_cidrs_json="$2"
  local configured_primary="${3:-}"
  local configured_standby="${4:-}"
  local base_json
  local candidates=(
    "172.31.240.0/29"
    "172.31.241.0/29"
    "172.31.242.0/29"
    "172.31.243.0/29"
    "172.31.244.0/29"
    "172.31.245.0/29"
    "172.31.246.0/29"
    "172.31.247.0/29"
    "172.31.248.0/29"
    "172.31.249.0/29"
  )
  local primary="$configured_primary"
  local standby="$configured_standby"
  local candidate

  base_json="$(python3 -c 'import json, sys
items = []
for raw in sys.argv[1:]:
    if not raw:
        continue
    items.extend([entry for entry in json.loads(raw) if entry])
print(json.dumps(items))' \
    "$primary_vcn_cidrs_json" \
    "$standby_vcn_cidrs_json")"

  if [[ -n "$primary" ]] && ! cidr_overlap_check "$primary" "$base_json"; then
    fail "Configured PRIMARY_HUB_VCN_CIDR $primary overlaps with an existing primary or standby VCN CIDR."
  fi

  if [[ -n "$standby" ]] && ! cidr_overlap_check "$standby" "$base_json"; then
    fail "Configured STANDBY_HUB_VCN_CIDR $standby overlaps with an existing primary or standby VCN CIDR."
  fi

  if [[ -n "$primary" && -n "$standby" ]]; then
    if ! cidr_overlap_check "$primary" "$(python3 -c 'import json, sys; print(json.dumps([sys.argv[1]]))' "$standby")"; then
      fail "Configured PRIMARY_HUB_VCN_CIDR $primary overlaps with STANDBY_HUB_VCN_CIDR $standby."
    fi
    printf '%s\n%s\n' "$primary" "$standby"
    return 0
  fi

  if [[ -n "$primary" ]]; then
    base_json="$(python3 -c 'import json, sys; items=json.loads(sys.argv[1]); items.append(sys.argv[2]); print(json.dumps(items))' "$base_json" "$primary")"
  fi
  if [[ -n "$standby" ]]; then
    base_json="$(python3 -c 'import json, sys; items=json.loads(sys.argv[1]); items.append(sys.argv[2]); print(json.dumps(items))' "$base_json" "$standby")"
  fi

  if [[ -z "$primary" ]]; then
    for candidate in "${candidates[@]}"; do
      if cidr_overlap_check "$candidate" "$base_json"; then
        primary="$candidate"
        base_json="$(python3 -c 'import json, sys; items=json.loads(sys.argv[1]); items.append(sys.argv[2]); print(json.dumps(items))' "$base_json" "$primary")"
        break
      fi
    done
    [[ -n "$primary" ]] || fail "Could not auto-pick PRIMARY_HUB_VCN_CIDR. Set it explicitly in exadb_dr_network.conf."
  fi

  if [[ -z "$standby" ]]; then
    for candidate in "${candidates[@]}"; do
      if cidr_overlap_check "$candidate" "$base_json"; then
        standby="$candidate"
        break
      fi
    done
    [[ -n "$standby" ]] || fail "Could not auto-pick STANDBY_HUB_VCN_CIDR. Set it explicitly in exadb_dr_network.conf."
  fi

  printf '%s\n%s\n' "$primary" "$standby"
}

wait_for_field() {
  local region="$1"
  local kind="$2"
  local ocid="$3"
  local field="$4"
  local expected="$5"
  local attempts="${6:-30}"
  local delay="${7:-10}"
  local get_cmd=()
  local current=""
  local attempt

  case "$kind" in
    vcn) get_cmd=(network vcn get --vcn-id "$ocid") ;;
    lpg) get_cmd=(network local-peering-gateway get --local-peering-gateway-id "$ocid") ;;
    drg) get_cmd=(network drg get --drg-id "$ocid") ;;
    rpc) get_cmd=(network remote-peering-connection get --remote-peering-connection-id "$ocid") ;;
    drg-attachment) get_cmd=(network drg-attachment get --drg-attachment-id "$ocid") ;;
    *) fail "Unsupported wait_for_field kind: $kind" ;;
  esac

  for ((attempt = 1; attempt <= attempts; attempt++)); do
    current="$(oci_raw "$region" "${get_cmd[@]}" --query "data.\"$field\"" 2>/dev/null || true)"
    if [[ "$current" == "$expected" ]]; then
      return 0
    fi
    sleep "$delay"
  done

  fail "Timed out waiting for $kind $ocid field $field to become $expected. Last value: ${current:-unknown}"
}

wait_for_attachment_route_table() {
  local region="$1"
  local drg_attachment_id="$2"
  local expected_route_table_id="$3"
  local attempts="${4:-30}"
  local delay="${5:-10}"
  local current=""
  local attempt

  for ((attempt = 1; attempt <= attempts; attempt++)); do
    current="$(oci_raw "$region" network drg-attachment get --drg-attachment-id "$drg_attachment_id" --query 'data."route-table-id"' 2>/dev/null || true)"
    if [[ "$current" == "$expected_route_table_id" ]]; then
      return 0
    fi
    sleep "$delay"
  done

  fail "Timed out waiting for DRG attachment $drg_attachment_id to use VCN route table $expected_route_table_id. Last value: ${current:-unknown}"
}

append_unique_word() {
  local current="${1-}"
  local new_word="$2"
  local word
  for word in $current; do
    if [[ "$word" == "$new_word" ]]; then
      printf '%s\n' "$current"
      return 0
    fi
  done
  if [[ -n "$current" ]]; then
    printf '%s %s\n' "$current" "$new_word"
  else
    printf '%s\n' "$new_word"
  fi
}

discover_subnet() {
  local region="$1"
  local compartment_id="$2"
  local vcn_id="$3"
  local cidr="$4"
  local label="$5"
  local subnet_json
  subnet_json="$(oci_json "$region" network subnet list --compartment-id "$compartment_id" --vcn-id "$vcn_id" --all)"

  json_query_arg "$subnet_json" 'import json, sys
payload = json.loads(sys.argv[1])
cidr = sys.argv[2]
label = sys.argv[3]
matches = [item for item in payload.get("data", []) if item.get("cidr-block") == cidr]
if not matches:
    raise SystemExit(f"Missing required {label} subnet with CIDR {cidr}")
if len(matches) != 1:
    raise SystemExit(f"Ambiguous {label} subnet lookup for CIDR {cidr}: found {len(matches)} matches")
item = matches[0]
print(item["id"])
print(item["route-table-id"])
print(item.get("display-name", ""))
' "$cidr" "$label"
}

route_rules_merged_file() {
  local current_rules_json="$1"
  local destination="$2"
  local network_entity_id="$3"
  local description="$4"
  local out_file="$5"
  python3 -c 'import json, sys
rules = json.loads(sys.argv[1])
destination = sys.argv[2]
entity = sys.argv[3]
description = sys.argv[4]
out_file = sys.argv[5]
exists = any(item.get("destination") == destination and item.get("network-entity-id") == entity for item in rules)
if not exists:
    rules.append({
        "description": description,
        "destination": destination,
        "destination-type": "CIDR_BLOCK",
        "network-entity-id": entity
    })
with open(out_file, "w", encoding="utf-8") as handle:
    json.dump(rules, handle, indent=2, sort_keys=True)
' "$current_rules_json" "$destination" "$network_entity_id" "$description" "$out_file"
}

update_route_table_add_rule() {
  local region="$1"
  local route_table_id="$2"
  local destination="$3"
  local network_entity_id="$4"
  local description="$5"
  local current_rules_json
  local merged_file

  current_rules_json="$(oci_json "$region" network route-table get --rt-id "$route_table_id" --query 'data."route-rules"')"
  merged_file="$(make_tmp_json)"
  route_rules_merged_file "$current_rules_json" "$destination" "$network_entity_id" "$description" "$merged_file"
  oci --region "$region" network route-table update --rt-id "$route_table_id" --route-rules "file://$merged_file" --force >/dev/null
}

create_nsg_rules_file() {
  local remote_client_cidr="$1"
  local remote_backup_cidr="$2"
  local allow_ssh="$3"
  local tcp_listener_port="$4"
  local tcps_listener_port="$5"
  local out_file="$6"
  python3 -c 'import json, sys
remote_client = sys.argv[1]
remote_backup = sys.argv[2]
allow_ssh = sys.argv[3] == "true"
tcp_listener_port = sys.argv[4]
tcps_listener_port = sys.argv[5]
out_file = sys.argv[6]
cidrs = [remote_client, remote_backup]
oracle_ports = []
if tcp_listener_port:
    oracle_ports.append(int(tcp_listener_port))
if tcps_listener_port:
    oracle_ports.append(int(tcps_listener_port))
rules = []
for cidr in cidrs:
    for port in oracle_ports:
        rules.append({
            "direction": "INGRESS",
            "is-stateless": False,
            "protocol": "6",
            "source": cidr,
            "source-type": "CIDR_BLOCK",
            "tcp-options": {"destination-port-range": {"min": port, "max": port}},
            "description": f"Allow Oracle Net ingress from {cidr} to port {port}"
        })
        rules.append({
            "direction": "EGRESS",
            "is-stateless": False,
            "protocol": "6",
            "destination": cidr,
            "destination-type": "CIDR_BLOCK",
            "tcp-options": {"destination-port-range": {"min": port, "max": port}},
            "description": f"Allow Oracle Net egress to {cidr} on port {port}"
        })
if allow_ssh:
    for cidr in cidrs:
        rules.append({
            "direction": "INGRESS",
            "is-stateless": False,
            "protocol": "6",
            "source": cidr,
            "source-type": "CIDR_BLOCK",
            "tcp-options": {"destination-port-range": {"min": 22, "max": 22}},
            "description": f"Allow SSH ingress from {cidr}"
        })
        rules.append({
            "direction": "EGRESS",
            "is-stateless": False,
            "protocol": "6",
            "destination": cidr,
            "destination-type": "CIDR_BLOCK",
            "tcp-options": {"destination-port-range": {"min": 22, "max": 22}},
            "description": f"Allow SSH egress to {cidr}"
        })
with open(out_file, "w", encoding="utf-8") as handle:
    json.dump(rules, handle, indent=2, sort_keys=True)
' "$remote_client_cidr" "$remote_backup_cidr" "$allow_ssh" "$tcp_listener_port" "$tcps_listener_port" "$out_file"
}

