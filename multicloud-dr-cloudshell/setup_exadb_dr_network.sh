drg_route_rule_exists() {
  local region="$1"
  local drg_route_table_id="$2"
  local destination="$3"
  local next_hop_attachment_id="$4"
  local rules_json
  rules_json="$(oci_json "$region" network drg-route-rule list --drg-route-table-id "$drg_route_table_id" --all)"
  python3 -c 'import json, sys
payload = json.loads(sys.argv[1])
destination = sys.argv[2]
next_hop = sys.argv[3]
items = payload.get("data", payload if isinstance(payload, list) else [])
for item in items:
    if item.get("destination") == destination and item.get("next-hop-drg-attachment-id") == next_hop:
        raise SystemExit(0)
raise SystemExit(1)
' "$rules_json" "$destination" "$next_hop_attachment_id"
}

update_drg_route_table_add_rule() {
  local region="$1"
  local drg_route_table_id="$2"
  local destination="$3"
  local next_hop_attachment_id="$4"
  local payload_file
  if drg_route_rule_exists "$region" "$drg_route_table_id" "$destination" "$next_hop_attachment_id"; then
    return 0
  fi
  payload_file="$(make_tmp_json)"
  create_drg_route_rule_payload "$destination" "$next_hop_attachment_id" "$payload_file"
  oci --region "$region" network drg-route-rule add \
    --drg-route-table-id "$drg_route_table_id" \
    --route-rules "file://$payload_file" >/dev/null
}

validate_vcn_belongs_to_compartment() {
  local region="$1"
  local vcn_id="$2"
  local expected_compartment_id="$3"
  local label="$4"
  local actual_compartment_id
  actual_compartment_id="$(oci_raw "$region" network vcn get --vcn-id "$vcn_id" --query 'data."compartment-id"' 2>/dev/null || true)"
  [[ -n "$actual_compartment_id" ]] || fail "Configured $label VCN does not exist or is not accessible: $vcn_id"
  [[ "$actual_compartment_id" == "$expected_compartment_id" ]] || fail "Configured $label VCN $vcn_id is in compartment $actual_compartment_id, expected $expected_compartment_id"
}

load_config() {
  [[ -f "$CONFIG_FILE" ]] || fail "Config file not found: $CONFIG_FILE"
  local config_exports
  if ! config_exports="$(
    python3 -c 'import configparser, shlex, sys
config = configparser.ConfigParser()
config.optionxform = str
if not config.read(sys.argv[1]):
    raise SystemExit(f"Could not read config file: {sys.argv[1]}")
def normalize(value):
    value = value.strip()
    if len(value) >= 2 and value[0] == value[-1] and value[0] in "\"\x27":
        value = value[1:-1]
    return value.strip()
for section in config.sections():
    for key, value in config.items(section):
        print(f"{key}={shlex.quote(normalize(value))}")
' "$CONFIG_FILE" 2>&1
  )"; then
    fail "Failed to parse config file $CONFIG_FILE: $config_exports"
  fi
  eval "$config_exports"
}

validate_required_config() {
  local required_vars=(
    PRIMARY_REGION PRIMARY_COMPARTMENT_OCID PRIMARY_VCN_OCID PRIMARY_CLIENT_SUBNET_CIDR PRIMARY_BACKUP_SUBNET_CIDR
    STANDBY_REGION STANDBY_COMPARTMENT_OCID STANDBY_VCN_OCID STANDBY_CLIENT_SUBNET_CIDR STANDBY_BACKUP_SUBNET_CIDR
  )
  local var_name
  for var_name in "${required_vars[@]}"; do
    [[ -n "${!var_name:-}" ]] || fail "Required config value is missing: $var_name"
  done
  [[ "$PRIMARY_REGION" != "$STANDBY_REGION" ]] || fail "PRIMARY_REGION and STANDBY_REGION must be different."
}

rpc_is_peered_with_expected_peer() {
  local region="$1"
  local rpc_id="$2"
  local expected_peer_id="$3"
  local expected_peer_region="$4"
  local status
  local peer_id
  local peer_region
  status="$(oci_raw "$region" network remote-peering-connection get --remote-peering-connection-id "$rpc_id" --query 'data."peering-status"' 2>/dev/null || true)"
  peer_id="$(oci_raw "$region" network remote-peering-connection get --remote-peering-connection-id "$rpc_id" --query 'data."peer-id"' 2>/dev/null || true)"
  peer_region="$(oci_raw "$region" network remote-peering-connection get --remote-peering-connection-id "$rpc_id" --query 'data."peer-region-name"' 2>/dev/null || true)"
  [[ "$status" == "PEERED" && "$peer_id" == "$expected_peer_id" && "$peer_region" == "$expected_peer_region" ]]
}

wait_for_rpc_peer_details() {
  local region="$1"
  local rpc_id="$2"
  local expected_peer_id="$3"
  local expected_peer_region="$4"
  local attempts="${5:-18}"
  local delay="${6:-5}"
  local attempt
  for ((attempt = 1; attempt <= attempts; attempt++)); do
    if rpc_is_peered_with_expected_peer "$region" "$rpc_id" "$expected_peer_id" "$expected_peer_region"; then
      return 0
    fi
    sleep "$delay"
  done
  return 1
}

connect_rpc_pair() {
  local requestor_region="$1"
  local requestor_rpc_id="$2"
  local acceptor_region="$3"
  local acceptor_rpc_id="$4"
  log "INFO" "Connecting RPC $requestor_rpc_id in $requestor_region to RPC $acceptor_rpc_id in $acceptor_region."
  oci --region "$requestor_region" network remote-peering-connection connect \
    --remote-peering-connection-id "$requestor_rpc_id" \
    --peer-id "$acceptor_rpc_id" \
    --peer-region-name "$acceptor_region" >/dev/null
}

ensure_rpc_peering() {
  local primary_region="$1"
  local primary_rpc_id="$2"
  local standby_region="$3"
  local standby_rpc_id="$4"
  connect_rpc_pair "$primary_region" "$primary_rpc_id" "$standby_region" "$standby_rpc_id"
  wait_for_field "$primary_region" "rpc" "$primary_rpc_id" "peering-status" "PEERED"
  wait_for_field "$standby_region" "rpc" "$standby_rpc_id" "peering-status" "PEERED"
  if wait_for_rpc_peer_details "$primary_region" "$primary_rpc_id" "$standby_rpc_id" "$standby_region" &&
     wait_for_rpc_peer_details "$standby_region" "$standby_rpc_id" "$primary_rpc_id" "$primary_region"; then
    return 0
  fi
  fail "RPCs reached PEERED state but peer metadata did not converge as expected. Primary RPC: $primary_rpc_id. Standby RPC: $standby_rpc_id."
}

start_rpc_peering_background() {
  local primary_region="$1"
  local primary_rpc_id="$2"
  local standby_region="$3"
  local standby_rpc_id="$4"
  local status_file
  status_file="$(make_tmp_env)"
  RPC_PEERING_STATUS_FILE="$status_file"
  (
    set +e
    ensure_rpc_peering "$primary_region" "$primary_rpc_id" "$standby_region" "$standby_rpc_id"
    printf '%s\n' "$?" >"$status_file"
  ) &
  RPC_PEERING_PID="$!"
  log "INFO" "Started background RPC peering monitor with PID $RPC_PEERING_PID."
}

wait_for_rpc_peering_background() {
  local status
  [[ -n "${RPC_PEERING_PID:-}" ]] || fail "Internal error: RPC peering background PID is not set."
  log "INFO" "Waiting for background RPC peering task to finish."
  wait "$RPC_PEERING_PID" || true
  [[ -f "$RPC_PEERING_STATUS_FILE" ]] || fail "Background RPC peering status file was not created: $RPC_PEERING_STATUS_FILE"
  status="$(tr -d '[:space:]' <"$RPC_PEERING_STATUS_FILE")"
  [[ "$status" == "0" ]] || fail "Background RPC peering task failed with exit code ${status:-unknown}."
  log "INFO" "Background RPC peering task finished successfully."
}

verify_rpc_belongs_to_expected_drg() {
  local region="$1"
  local rpc_id="$2"
  local expected_drg_id="$3"
  local label="$4"
  local actual_drg_id
  local lifecycle_state
  actual_drg_id="$(oci_raw "$region" network remote-peering-connection get --remote-peering-connection-id "$rpc_id" --query 'data."drg-id"')"
  lifecycle_state="$(oci_raw "$region" network remote-peering-connection get --remote-peering-connection-id "$rpc_id" --query 'data."lifecycle-state"')"
  [[ "$actual_drg_id" == "$expected_drg_id" ]] || fail "The $label RPC $rpc_id is attached to DRG $actual_drg_id, expected $expected_drg_id."
  [[ "$lifecycle_state" == "AVAILABLE" ]] || fail "The $label RPC $rpc_id is not AVAILABLE. Current lifecycle state: $lifecycle_state"
}

find_rpc_drg_attachment() {
  local region="$1"
  local compartment_id="$2"
  local drg_id="$3"
  local rpc_id="$4"
  local label="$5"
  local attachment_json
  attachment_json="$(oci_json "$region" network drg-attachment list \
    --compartment-id "$compartment_id" \
    --drg-id "$drg_id" \
    --attachment-type REMOTE_PEERING_CONNECTION \
    --all)"
  json_query_arg "$attachment_json" 'import json, sys
payload = json.loads(sys.argv[1])
rpc_id = sys.argv[2]
label = sys.argv[3]
items = payload.get("data", [])
matches = []
for item in items:
    network_details = item.get("network-details", item.get("networkDetails", {}))
    network_id = (
        item.get("network-id")
        or item.get("networkId")
        or network_details.get("id")
        or network_details.get("network-id")
        or network_details.get("networkId")
    )
    lifecycle_state = item.get("lifecycle-state", item.get("lifecycleState", ""))
    if network_id == rpc_id and lifecycle_state in ("ATTACHED", "AVAILABLE", ""):
        matches.append(item)
if not matches:
    raise SystemExit(f"Could not find autogenerated RPC DRG attachment for {label} RPC {rpc_id}")
if len(matches) != 1:
    raise SystemExit(f"Ambiguous autogenerated RPC DRG attachment lookup for {label} RPC {rpc_id}: found {len(matches)} matches")
item = matches[0]
attachment_id = item.get("id", "")
drg_route_table_id = item.get("drg-route-table-id", item.get("drgRouteTableId", ""))
if not attachment_id or not drg_route_table_id:
    raise SystemExit(f"Autogenerated RPC DRG attachment for {label} RPC {rpc_id} is missing attachment metadata")
print(attachment_id)
print(drg_route_table_id)
' "$rpc_id" "$label"
}

wait_for_rpc_attachment_to_drg() {
  local region="$1"
  local compartment_id="$2"
  local drg_id="$3"
  local rpc_id="$4"
  local label="$5"
  local attempts="${6:-24}"
  local delay="${7:-5}"
  local attempt
  local attachment_output
  for ((attempt = 1; attempt <= attempts; attempt++)); do
    attachment_output="$(find_rpc_drg_attachment "$region" "$compartment_id" "$drg_id" "$rpc_id" "$label" 2>/dev/null || true)"
    if [[ -n "$attachment_output" ]]; then
      printf '%s\n' "$attachment_output"
      return 0
    fi
    log "INFO" "Waiting for the autogenerated DRG attachment for the $label RPC in $region (attempt $attempt/$attempts)."
    sleep "$delay"
  done
  fail "Timed out waiting for the $label RPC $rpc_id to have an autogenerated DRG attachment on DRG $drg_id in region $region."
}

wait_for_bg_pid() {
  local pid="$1"
  local label="$2"
  if ! wait "$pid"; then
    fail "$label failed."
  fi
}

