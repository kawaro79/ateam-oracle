create_region_local_artifacts_workstream() {
  local prefix="$1"
  local label="$2"
  local region="$3"
  local compartment_id="$4"
  local exadata_vcn_id="$5"
  local hub_cidr="$6"
  local hub_vcn_name="$7"
  local hub_drg_rt_name="$8"
  local hub_lpg_rt_name="$9"
  local exadata_lpg_name="${10}"
  local hub_lpg_name="${11}"
  local drg_id="${12}"
  local out_file="${13}"
  local hub_vcn_id
  local hub_default_route_table_id
  local hub_drg_route_table_id
  local hub_lpg_route_table_id
  local exadata_lpg_id
  local hub_lpg_id
  local attachment_payload
  local drg_attachment_id
  local dns_prefix

  dns_prefix="$(printf '%s' "$label" | cut -c1-1)"
  log "INFO" "[$label] Creating hub VCN."
  hub_vcn_id="$(oci_raw "$region" network vcn create \
    --compartment-id "$compartment_id" \
    --cidr-block "$hub_cidr" \
    --display-name "$hub_vcn_name" \
    --dns-label "${dns_prefix}h${RUN_TS:9:6}" \
    --query 'data.id')"
  wait_for_field "$region" "vcn" "$hub_vcn_id" "lifecycle-state" "AVAILABLE"
  hub_default_route_table_id="$(oci_raw "$region" network vcn get --vcn-id "$hub_vcn_id" --query 'data."default-route-table-id"')"

  log "INFO" "[$label] Creating hub route tables."
  hub_drg_route_table_id="$(oci_raw "$region" network route-table create --compartment-id "$compartment_id" --vcn-id "$hub_vcn_id" --display-name "$hub_drg_rt_name" --route-rules '[]' --query 'data.id')"
  hub_lpg_route_table_id="$(oci_raw "$region" network route-table create --compartment-id "$compartment_id" --vcn-id "$hub_vcn_id" --display-name "$hub_lpg_rt_name" --route-rules '[]' --query 'data.id')"

  log "INFO" "[$label] Creating and peering LPGs."
  exadata_lpg_id="$(oci_raw "$region" network local-peering-gateway create --compartment-id "$compartment_id" --vcn-id "$exadata_vcn_id" --display-name "$exadata_lpg_name" --query 'data.id')"
  wait_for_field "$region" "lpg" "$exadata_lpg_id" "lifecycle-state" "AVAILABLE"
  hub_lpg_id="$(oci_raw "$region" network local-peering-gateway create --compartment-id "$compartment_id" --vcn-id "$hub_vcn_id" --display-name "$hub_lpg_name" --query 'data.id')"
  wait_for_field "$region" "lpg" "$hub_lpg_id" "lifecycle-state" "AVAILABLE"
  oci --region "$region" network local-peering-gateway update --local-peering-gateway-id "$hub_lpg_id" --route-table-id "$hub_lpg_route_table_id" --force >/dev/null
  oci --region "$region" network local-peering-gateway connect --local-peering-gateway-id "$hub_lpg_id" --peer-id "$exadata_lpg_id" >/dev/null
  wait_for_field "$region" "lpg" "$hub_lpg_id" "peering-status" "PEERED"
  wait_for_field "$region" "lpg" "$exadata_lpg_id" "peering-status" "PEERED"

  log "INFO" "[$label] Attaching hub VCN to DRG."
  attachment_payload="$(make_tmp_json)"
  create_drg_attachment_payload "$hub_vcn_id" "$attachment_payload"
  drg_attachment_id="$(oci_raw "$region" network drg-attachment create --drg-id "$drg_id" --network-details "file://$attachment_payload" --display-name "${NAME_PREFIX}-${label}-hub-attachment-${RUN_TS}" --query 'data.id')"
  wait_for_field "$region" "drg-attachment" "$drg_attachment_id" "lifecycle-state" "ATTACHED"
  oci --region "$region" network drg-attachment update --drg-attachment-id "$drg_attachment_id" --route-table-id "$hub_drg_route_table_id" --force >/dev/null
  wait_for_attachment_route_table "$region" "$drg_attachment_id" "$hub_drg_route_table_id"

  {
    printf '%s=%q\n' "${prefix}_HUB_VCN_ID" "$hub_vcn_id"
    printf '%s=%q\n' "${prefix}_HUB_DEFAULT_ROUTE_TABLE_ID" "$hub_default_route_table_id"
    printf '%s=%q\n' "${prefix}_HUB_DRG_ROUTE_TABLE_ID" "$hub_drg_route_table_id"
    printf '%s=%q\n' "${prefix}_HUB_LPG_ROUTE_TABLE_ID" "$hub_lpg_route_table_id"
    printf '%s=%q\n' "${prefix}_EXADATA_LPG_ID" "$exadata_lpg_id"
    printf '%s=%q\n' "${prefix}_HUB_LPG_ID" "$hub_lpg_id"
    printf '%s=%q\n' "${prefix}_DRG_ATTACHMENT_ID" "$drg_attachment_id"
  } >"$out_file"
}

configure_region_post_peering_workstream() {
  local prefix="$1"
  local label="$2"
  local region="$3"
  local compartment_id="$4"
  local exadata_vcn_id="$5"
  local local_client_cidr="$6"
  local local_backup_cidr="$7"
  local remote_client_cidr="$8"
  local remote_backup_cidr="$9"
  local exadata_route_table_ids="${10}"
  local hub_drg_route_table_id="${11}"
  local hub_lpg_route_table_id="${12}"
  local hub_default_route_table_id="${13}"
  local hub_lpg_id="${14}"
  local drg_id="${15}"
  local exadata_lpg_id="${16}"
  local rpc_id="${17}"
  local drg_attachment_id="${18}"
  local nsg_name="${19}"
  local allow_ssh="${20}"
  local tcp_listener_port="${21}"
  local tcps_listener_port="${22}"
  local drg_for_rpc="${23}"
  local out_file="${24}"
  local rpc_attachment_info=()
  local rpc_attachment_id
  local rpc_drg_route_table_id
  local route_table_id
  local nsg_id
  local nsg_rules_file

  mapfile -t rpc_attachment_info < <(wait_for_rpc_attachment_to_drg "$region" "$compartment_id" "$drg_for_rpc" "$rpc_id" "$label")
  [[ ${#rpc_attachment_info[@]} -ge 2 ]] || fail "Failed to resolve the $label RPC DRG attachment for RPC $rpc_id after peering."
  rpc_attachment_id="${rpc_attachment_info[0]}"
  rpc_drg_route_table_id="${rpc_attachment_info[1]}"

  log "INFO" "[$label] Updating route tables."
  update_route_table_add_rule "$region" "$hub_drg_route_table_id" "$local_client_cidr" "$hub_lpg_id" "Managed by $DESCRIPTION_TOKEN to local client subnet via ${label} hub LPG"
  update_route_table_add_rule "$region" "$hub_drg_route_table_id" "$local_backup_cidr" "$hub_lpg_id" "Managed by $DESCRIPTION_TOKEN to local backup subnet via ${label} hub LPG"
  update_route_table_add_rule "$region" "$hub_lpg_route_table_id" "$remote_client_cidr" "$drg_id" "Managed by $DESCRIPTION_TOKEN to remote client subnet via ${label} DRG"
  update_route_table_add_rule "$region" "$hub_lpg_route_table_id" "$remote_backup_cidr" "$drg_id" "Managed by $DESCRIPTION_TOKEN to remote backup subnet via ${label} DRG"
  update_route_table_add_rule "$region" "$hub_default_route_table_id" "$local_client_cidr" "$hub_lpg_id" "Managed by $DESCRIPTION_TOKEN to local client subnet via ${label} hub LPG"
  update_route_table_add_rule "$region" "$hub_default_route_table_id" "$local_backup_cidr" "$hub_lpg_id" "Managed by $DESCRIPTION_TOKEN to local backup subnet via ${label} hub LPG"
  update_route_table_add_rule "$region" "$hub_default_route_table_id" "$remote_client_cidr" "$drg_id" "Managed by $DESCRIPTION_TOKEN to remote client subnet via ${label} DRG"
  update_route_table_add_rule "$region" "$hub_default_route_table_id" "$remote_backup_cidr" "$drg_id" "Managed by $DESCRIPTION_TOKEN to remote backup subnet via ${label} DRG"
  for route_table_id in $exadata_route_table_ids; do
    update_route_table_add_rule "$region" "$route_table_id" "$remote_client_cidr" "$exadata_lpg_id" "Managed by $DESCRIPTION_TOKEN to remote client subnet via ${label} LPG"
    update_route_table_add_rule "$region" "$route_table_id" "$remote_backup_cidr" "$exadata_lpg_id" "Managed by $DESCRIPTION_TOKEN to remote backup subnet via ${label} LPG"
  done
  update_drg_route_table_add_rule "$region" "$rpc_drg_route_table_id" "$local_client_cidr" "$drg_attachment_id"
  update_drg_route_table_add_rule "$region" "$rpc_drg_route_table_id" "$local_backup_cidr" "$drg_attachment_id"

  log "INFO" "[$label] Creating NSG."
  nsg_id="$(oci_raw "$region" network nsg create --compartment-id "$compartment_id" --vcn-id "$exadata_vcn_id" --display-name "$nsg_name" --query 'data.id')"
  nsg_rules_file="$(make_tmp_json)"
  create_nsg_rules_file "$remote_client_cidr" "$remote_backup_cidr" "$allow_ssh" "$tcp_listener_port" "$tcps_listener_port" "$nsg_rules_file"
  oci --region "$region" network nsg rules add --nsg-id "$nsg_id" --security-rules "file://$nsg_rules_file" >/dev/null

  {
    printf '%s=%q\n' "${prefix}_RPC_ATTACHMENT_ID" "$rpc_attachment_id"
    printf '%s=%q\n' "${prefix}_RPC_DRG_ROUTE_TABLE_ID" "$rpc_drg_route_table_id"
    printf '%s=%q\n' "${prefix}_NSG_ID" "$nsg_id"
  } >"$out_file"
}

